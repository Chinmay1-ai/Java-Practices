package com.sct;

public class Q11 {
	public static void main(String[] args) {
		int a = 1;

		switch (a) {

		case 1:
			System.out.println("Start");
			break;
		case 2:
			System.out.println("Stop");
			break;
		case 3:
			System.out.println("Restart");
			break;
		default:
			System.out.println("Invalid Choice");
			break;
		}
	}
}
