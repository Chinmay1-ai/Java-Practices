package com.sct;

public class Q7 {
	public static void main(String[] args) {
		int season = 4;

		switch (season) {

		case 1:
			System.out.println("Summer");
			break;
		case 2:
			System.out.println("Rainy");
			break;
		case 3:
			System.out.println("Winter");
			break;
		case 4:
			System.out.println("Spring");
			break;
		default:
			System.out.println("Invalid Choice of Season");
			break;
		}
	}
}
