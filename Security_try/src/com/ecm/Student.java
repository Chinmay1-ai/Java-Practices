package com.ecm;

public class Student {

	private int id;
	private String fName;
	private String lName;
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getFname() {
		return fName;
	}
	
	public void setFname(String fName) {
		this.fName = fName;
	}
	public String getLname() {
		return lName;
	}
	
	public void setLname(String lName) {
		this.lName = lName;
	}
}
