package Operators;

public class Relational {
public static void main(String[] args) {
	int num  = 12;
	int num1 = 45;
	int num2 = 85;
	int num3 = 54;
	
	float  num4 = 12.5f;
	double num5 = 222.4;
	
//	So Basically it will give output in Boolean form like (True/False)
	
	System.out.println(num>num1);
	System.out.println(num2<num3);
	
	System.out.println(num4>=num5);
	System.out.println(num3<=num);
	System.out.println(num==num4);
	System.out.println(num!=num1);
}
}
